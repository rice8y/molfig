#!/usr/bin/env node

import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { spawnSync } from 'node:child_process';
import { fileURLToPath } from 'node:url';

const scriptDir = dirname(fileURLToPath(import.meta.url));
const pluginDir = resolve(scriptDir, '..');
const sourcePath = resolve(
    pluginDir,
    'artifacts/molstar/src/mol-geo/util/marching-cubes/tables.ts',
);
const outputPath = resolve(pluginDir, 'src/mesh/surface_tables.rs');
const source = readFileSync(sourcePath, 'utf8');

function readArray(name, followingMarker) {
    const startMarker = `export const ${name} = `;
    const start = source.indexOf(startMarker);
    if (start < 0) throw new Error(`Could not find ${name} in ${sourcePath}`);

    const valueStart = start + startMarker.length;
    const end = source.indexOf(followingMarker, valueStart);
    if (end < 0) throw new Error(`Could not find the end of ${name} in ${sourcePath}`);

    const expression = source.slice(valueStart, end).trim().replace(/;$/, '');
    return Function(`"use strict"; return (${expression});`)();
}

const edgeTable = readArray('EdgeTable', '\n\nexport const TriTable');
const triTable = readArray('TriTable', '\n\n/**\n * Triangles are constructed');

if (edgeTable.length !== 256 || triTable.length !== 256) {
    throw new Error(
        `Unexpected Mol* table sizes: EdgeTable=${edgeTable.length}, TriTable=${triTable.length}`,
    );
}

function formatRows(values, width, formatter) {
    const rows = [];
    for (let i = 0; i < values.length; i += width) {
        rows.push(`    ${values.slice(i, i + width).map(formatter).join(', ')},`);
    }
    return rows.join('\n');
}

const triRows = triTable
    .map((row) => `    &[${row.join(', ')}],`)
    .join('\n');

const output = `// @generated by scripts/generate-marching-cubes-tables.mjs.
//
// Derived from Mol* 5.9.0:
// src/mol-geo/util/marching-cubes/tables.ts
//
// Copyright (c) 2018 Mol* contributors.
// Licensed under the MIT License. See the Mol* LICENSE file for details.

#[derive(Clone, Copy)]
pub(super) struct Index {
    pub(super) i: usize,
    pub(super) j: usize,
    pub(super) k: usize,
}

#[derive(Clone, Copy)]
pub(super) struct IndexPair {
    pub(super) a: Index,
    pub(super) b: Index,
}

#[derive(Clone, Copy)]
pub(super) struct EdgeId {
    pub(super) i: usize,
    pub(super) j: usize,
    pub(super) k: usize,
    pub(super) edge: usize,
}

const fn index(i: usize, j: usize, k: usize) -> Index {
    Index { i, j, k }
}

const CUBE_VERTICES: [Index; 8] = [
    index(0, 0, 0),
    index(1, 0, 0),
    index(1, 1, 0),
    index(0, 1, 0),
    index(0, 0, 1),
    index(1, 0, 1),
    index(1, 1, 1),
    index(0, 1, 1),
];

pub(super) const CUBE_EDGES: [IndexPair; 12] = [
    IndexPair { a: CUBE_VERTICES[0], b: CUBE_VERTICES[1] },
    IndexPair { a: CUBE_VERTICES[1], b: CUBE_VERTICES[2] },
    IndexPair { a: CUBE_VERTICES[2], b: CUBE_VERTICES[3] },
    IndexPair { a: CUBE_VERTICES[3], b: CUBE_VERTICES[0] },
    IndexPair { a: CUBE_VERTICES[4], b: CUBE_VERTICES[5] },
    IndexPair { a: CUBE_VERTICES[5], b: CUBE_VERTICES[6] },
    IndexPair { a: CUBE_VERTICES[6], b: CUBE_VERTICES[7] },
    IndexPair { a: CUBE_VERTICES[7], b: CUBE_VERTICES[4] },
    IndexPair { a: CUBE_VERTICES[0], b: CUBE_VERTICES[4] },
    IndexPair { a: CUBE_VERTICES[1], b: CUBE_VERTICES[5] },
    IndexPair { a: CUBE_VERTICES[2], b: CUBE_VERTICES[6] },
    IndexPair { a: CUBE_VERTICES[3], b: CUBE_VERTICES[7] },
];

pub(super) const EDGE_ID_INFO: [EdgeId; 12] = [
    EdgeId { i: 0, j: 0, k: 0, edge: 0 },
    EdgeId { i: 1, j: 0, k: 0, edge: 1 },
    EdgeId { i: 0, j: 1, k: 0, edge: 0 },
    EdgeId { i: 0, j: 0, k: 0, edge: 1 },
    EdgeId { i: 0, j: 0, k: 1, edge: 0 },
    EdgeId { i: 1, j: 0, k: 1, edge: 1 },
    EdgeId { i: 0, j: 1, k: 1, edge: 0 },
    EdgeId { i: 0, j: 0, k: 1, edge: 1 },
    EdgeId { i: 0, j: 0, k: 0, edge: 2 },
    EdgeId { i: 1, j: 0, k: 0, edge: 2 },
    EdgeId { i: 1, j: 1, k: 0, edge: 2 },
    EdgeId { i: 0, j: 1, k: 0, edge: 2 },
];

// EdgeTable and TriTable ultimately originate from Paul Bourke's polygonise
// tables, as noted by the Mol* source.
pub(super) const EDGE_TABLE: [u16; 256] = [
${formatRows(edgeTable, 8, (value) => `0x${value.toString(16)}`)}
];

pub(super) const TRI_TABLE: [&[u8]; 256] = [
${triRows}
];
`;

writeFileSync(outputPath, output);
const rustfmt = spawnSync('rustfmt', ['--edition', '2021', outputPath], {
    stdio: 'inherit',
});
if (rustfmt.status !== 0) {
    throw new Error(`rustfmt failed with status ${rustfmt.status}`);
}
console.log(`Wrote ${outputPath}`);
